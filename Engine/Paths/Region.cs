using System;
using System.Collections.Generic;
using System.Text;

using Sokoban.Engine.Core;
using Sokoban.Engine.Levels;

namespace Sokoban.Engine.Paths
{
    public struct Region
    {
        private Coordinate2D coordinate;
        private int count;

        public Region(Coordinate2D coordinate, int count)
        {
            this.coordinate = coordinate;
            this.count = count;
        }

        public Coordinate2D Coordinate
        {
            get
            {
                return coordinate;
            }
            set
            {
                coordinate = value;
            }
        }

        public int Count
        {
            get
            {
                return count;
            }
            set
            {
                count = value;
            }
        }
    }
}
